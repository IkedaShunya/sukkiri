package sukkiri.lesson05.practice_print05.practice01;

public class Main {
	/*メソッド名： printHello
	引数：なし
	戻り値：なし
	処理の内容： "Hello" という文字を出力する
	ヒント：メソッドの定義は次のようになる
	public static void printHello() { 処理コード記入 }*/

	public static void main(String[] args) {
		printHello();

	}
	
	public static void printHello() {
		System.out.println("Hello");
	}

}
